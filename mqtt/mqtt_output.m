    s.voltage = block.InputPort(4).Data;
    s.ts = floor(posixtime(datetime('now','TimeZone','local')));
    s.id = block.DialogPrm(2).Data; % sensorID
    % END CUSTOMIZATION
    
    % to debug you can uncomment the following line
    % jsonencode(s)
    
    % send data in json format to the mqtt topic
    write(block.DialogPrm(1).Data, block.DialogPrm(3).Data, jsonencode(s));
end
